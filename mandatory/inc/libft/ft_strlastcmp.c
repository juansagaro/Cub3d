int	ft_strlastcmp(char *s1, char *s2)
{
	if (!s1 || !s2 || !*s1 || !*s2)
		return (0);
	while (*(s1 + 1))
		s1++;
	while (*(s2 + 1))
		s2++;
	return ((unsigned char)(*s1) - (unsigned char)(*s2));
}
