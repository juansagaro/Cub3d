#include "libft.h"

int	ft_sqrt(int n)
{
	int	i;

	if (n < 0)
		return (0);
	i = 0;
	while (i * i <= n)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (i - 1);
}
